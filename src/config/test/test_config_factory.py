import unittest
from config.config_factory import TFConfigFactory

class TestConfigFactory(unittest.TestCase):
    def setUp(self):
        self.config = TFConfigFactory.get(config_name="patent_domain")

    def test_deault_values(self):
        self.assertEquals(self.config.text_column, "word")
        self.assertEquals(self.config.entity_column, "entity_name")
